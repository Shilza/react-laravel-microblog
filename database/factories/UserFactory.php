$factory->define(User::class, function (Faker $faker) {
    return [
        'name' => $faker->name,
        'email' => $faker->unique()->safeEmail,
        'password' => Hash::make($faker->password),
        'refresh_token' => RefreshHelper::generateRefresh(User::count()+1)
    ];
});
